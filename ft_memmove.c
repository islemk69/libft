#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*dest;
	char	*source;

	dest = (char *)dst;
	source = (char *)src;
	if (dest > source)
		while (len--)
			dest[len] = source[len];
	else
		dest = ft_memcpy(dest, source, len);
	return (dst);
}
